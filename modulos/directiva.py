def pagina_multas():

    st.subheader("âš  AplicaciÃ³n de multas")

    con = obtener_conexion()
    if not con:
        st.error("âŒ No se pudo conectar a la base de datos.")
        return
    cursor = con.cursor()

    # Socia
    cursor.execute("SELECT Id_Socia, Nombre FROM Socia")
    datos = cursor.fetchall()
    socias = {s[1]: s[0] for s in datos}

    socia_sel = st.selectbox("ğŸ‘© Seleccione la socia:", list(socias.keys()))
    id_socia = socias[socia_sel]

    # Tipo de multa
    cursor.execute("SELECT Id_Tipo_multa, Tipo_de_multa FROM Tipo_de_multa")
    tipos = cursor.fetchall()
    tipos_multa = {t[1]: t[0] for t in tipos}

    tipo_sel = st.selectbox("ğŸ“Œ Tipo de multa:", list(tipos_multa.keys()))
    id_tipo = tipos_multa[tipo_sel]

    fecha = st.date_input("ğŸ“… Fecha de aplicaciÃ³n")
    monto = st.number_input("ğŸ’² Monto de la multa", min_value=0.0, step=0.50)
    estado = st.selectbox("ğŸ”– Estado:", ["A pagar", "Pagada"])

    # Guardar multa
    if st.button("ğŸ’¾ Guardar multa"):
        try:
            cursor.execute("""
                INSERT INTO Multa (Monto, Fecha_aplicacion, Estado, Id_Tipo_multa, Id_Socia)
                VALUES (%s, %s, %s, %s, %s)
            """, (monto, fecha, estado, id_tipo, id_socia))

            con.commit()
            st.success("âœ… Multa registrada correctamente.")
        except Exception as e:
            st.error(f"âš  Error: {e}")

    # Mostrar multas
    st.subheader("ğŸ“‹ Multas registradas")

    cursor.execute("""
        SELECT M.Id_Multa, S.Nombre, T.Tipo_de_multa, M.Monto, M.Estado, M.Fecha_aplicacion
        FROM Multa M
        JOIN Socia S ON S.Id_Socia = M.Id_Socia
        JOIN Tipo_de_multa T ON T.Id_Tipo_multa = M.Id_Tipo_multa
        ORDER BY M.Id_Multa DESC
    """)
    registros = cursor.fetchall()

    if registros:
        df = pd.DataFrame(registros, columns=["ID", "Socia", "Tipo", "Monto", "Estado", "Fecha"])
        st.dataframe(df, use_container_width=True)
    else:
        st.info("No hay multas registradas aÃºn.")


